public class Coordonnees {

	private int coordX;
	private int coordY;

	/*
	 * Getters et setters
	 */

	public int getPositionX() {
		return coordX;
	}

	public void setPositionX(int positionX) {
		this.coordX = positionX;
	}

	public int getPositionY() {
		return coordY;
	}

	public void setPositionY(int positionY) {
		this.coordY = positionY;
	}

}
